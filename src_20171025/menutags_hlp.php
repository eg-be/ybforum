<h2 align=center>Hilfe und Pseudotags Men&uuml;gestaltung</h2>

<p align=justify><b>Innerhalb der freien Gestaltung der Men&uuml;s des Forums können und sollten Sie 
die folgenden speziellen Pseudotags verwenden. Die Vorteile dieser Tags: Kompatibiltät zu kommenden 
Versionen und eine beschränkte Intelligenz, d.h. die Tags ergeben keinen Funktionslink in Men&uuml;s,
in denen Sie keinen Sinn machen (z.B. ist der Linke "Beitrag schreiben" im Men&uuml; "Beitrag schreiben" 
sinnlos.</b></p>

<center><table cellpadding=3 border=1>

<tr>
<td valign=top><b>Tag</b></td>
<td valign=top><b>Funktion</b></td>
</tr>

<tr>
<td valign=top><b>Tag</b></td>
<td valign=top><b>Funktion</b></td>
</tr>

<tr>
<td valign=top>[index]...[/index]</td>
<td valign=top><p align=justify>Link zur Startseite des Forums (index.php), ist immer aktiv, ausser 
auf der Startseite selber.</p></td>
</tr>

<tr>
<td valign=top>[recent]...[/recent]</td>
<td valign=top><p align=justify>Link zur Ansicht der neuesten Beitr&auml;ge des Forums (recent.php), ist immer 
aktiv, ausser auf der Seite der neuesten Beit&auml;ge selber.</p></td>
</tr>

<tr>
<td valign=top>[search]...[/search]</td>
<td valign=top><p align=justify>Link zur Suchfunktion (search.php), ist immer 
aktiv, ausser in der Suche selbst.</p></td>
</tr>

<tr>
<td valign=top>[post]...[/post]</td>
<td valign=top><p align=justify>Link zum Schreiben von Beitr&auml;gen. Dieser Link ist der intelligenteste: Auf 
der Startseite springt er in die Maske zum Beitragsschreiben und er&ouml;ffnet einen neuen Thread.  In der 
Ansich einer Beitrages springt er ins Beitragsschreiben und erzeugt eine Antwort zum soeben angezeiten Beitrag, 
solange der Thread nicht geschlossen ist. Auf einer Archivseite ist er nicht aktiv (ein Archiv ist ein solches 
und wird nicht mehr beschrieben), bei Anzeige eines Archivbeitrags wird eine Antwort zu diesem Beitrag als neuer 
Thread im laufenden Forum erzeugt, wenn der Thread im Archiv nicht als geschlossen gef&uuml;hrt wird.</p></td>
</tr>

<tr>
<td valign=top>[regulars]...[/regulars]</td>
<td valign=top><p align=justify>&Ouml;ffnet das Fenster für Stammposterlogin, aktiv auf allen Seiten, wenn die 
Stammposterfunktionalit&auml;t des Forums aktiv ist.</p></td>
</tr>

<tr>
<td valign=top>[format]...[/format]</td>
<td valign=top><p align=justify>&Ouml;ffnet die Hilfe zu Textformatierung von Beitragstexten, immer aktiv.</p></td>
</tr>

<tr>
<td valign=top>[NewView]...[/NewView]</td>
<td valign=top><p align=justify>Nur aktiv auf der Startseite, schaltet die Anordnung der Threads um (ein/aus). Im 
aktivierten Zustand wird der Thread am Anfang des Forums dargestellt, in dem das neueste Postings des 
Forums ist, der Thread mit dem zeitlich vorletzten Beitrag an zweiter Stelle usw. Die Reihenfolge der Beitr&auml;ge 
innerhalb des Threads wird dabei nicht ge&auml;ndert. In Archiven ist diese Funktion nicht aktiv.</p></td>
</tr>

<tr>
<td valign=top>[toggle]...[/toggle]</td>
<td valign=top><p align=justify>Nur aktiv auf der Startseite, schaltet zwischen den Ansichten nur Threads 
darstellen und alle Beitr&auml;ge darstellen um.</p></td>
</tr>

<tr>
<td valign=top>[atoggle]...[/atoggle]</td>
<td valign=top><p align=justify>Nur aktiv auf der Archivseiten, schaltet zwischen den Ansichten nur Threads 
darstellen und alle Beitr&auml;ge darstellen um.</p></td>
</tr>

<tr>
<td valign=top>[archive]...[/archive]</td>
<td valign=top><p align=justify>Springt beim Anzeigen von Archivbeiträgen oder beim Antworten auf einen 
Archivbeitrag auf die Archivseite zur&uuml;ck, ist nur in für die beiden genannten F&auml;lle aktiv.</p></td>
</tr>


</table></center>

<p align=justify>Alle Tags funktinoieren nach dem Prinzip, dass sie das, was Sie zwischen Tags setzen, 
also z.B. auch Bilder, als Linksymbol für die entsprechende Funktion einsetzen.<br>
<br>
Neben diesen Tags k&ouml;nnen Sie zur Menügestaltung frei HTML-Code verwenden. Ihre eigene Men&uuml;gestaltung 
ersetzt die Defaultmen&uuml;s wenn Sie l&auml;nger als 3 Zeichen ist.<br>
<br>
Tip/Hinweis: Sie k&ouml;nnen die Pseudotags auch in Men&uuml;s verwenden, in denen Sie nicht aktiv sind. Ihr 
Linksymbol wird trotzdem dargestellt, hat dann aber keine Linkfunktion. Auf diese Weise k&ouml;nnen Sie alle 
Men&uuml;s identisch gestalten und dabei sind immer nur die Funktionen aktiv, die an der entsprechenden Stelle 
sinnvoll sind.</p>

